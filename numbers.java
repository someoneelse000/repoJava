public class numbers {
	public static void main(String[]args){
		Integer a=10;
		System.out.println(a.compareTo(15));
		Integer b=11;
		System.out.println(b.equals(12));
		Integer c=12;
		System.out.println(c.toString());
		int d=Integer.parseInt("6");
		System.out.println(d);
		double e=10.6;
		System.out.println(Math.ceil(e));
		System.out.println(Math.floor(e));
		System.out.println(Math.rint(e));
		System.out.println(Math.round(e));
		System.out.println(Math.min(3,2,1));
		System.out.println(Math.max(1,2,3));
		System.out.println(Math.pow(2,4));
		System.out.println(Math.sqrt(125));
		System.out.println(Math.random());
	}
}
